# Для работы с асинхронностью нам понадобится эта библиотека.
# Пока просто импортируем ее, ее роль станет ясна чуть позже.
import asyncio


# 1. Объявляем асинхронную функцию с помощью "async def"
async def my_first_coroutine():
    print("Привет, я корутина! Мой код начал выполняться.")


# --- Основная часть программы ---

print("Вызываем корутину...")

# 2. Вызываем ее как обычную функцию
coroutine_object = my_first_coroutine()

print("Корутина вызвана.")
print("Тип возвращенного объекта:", type(coroutine_object))
print("Сам объект:", coroutine_object)